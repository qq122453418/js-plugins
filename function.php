/**
* 解析搜索参数
* @param String $str 要解析的参数字符串
* @param Booler $unempty 是否过滤掉空值
* @return array 返回解析后的参数
*/
function parse_param($str,$unempty = false){
	$p = array();
	if(!$str){
		return $p;
	}
	$a = explode('&', $str);
	if(!$a){
		return $p;
	}
	foreach($a as $kv){
		$b = explode('=', $kv);
		if($b && $b[0]){
			$k = urldecode($b[0]);
			if(isset($b[1])){
				$p[$k] = urldecode($b[1]);
			}else{
				$p[$k] = null;
			}
			if($unempty){
				if(check_empty($p[$k])){
					unset($p[$k]);
				}
			}
		}
	}
	return $p;
}

